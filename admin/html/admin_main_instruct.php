<h3>Overview:</h3>
<p>This administration section allows the owner of the website to make
changes to the content without having to know any HTML.  Nearly all the 
information on the website can be changed using the forms in this site.</p>
<p>Below is a listing of each section and what can be done.  Each form will
also have instructions.</p>

<h3>Main</h3>
<p>This is the Main Administration Page.  It is the page that comes
up when logging in, and it contains the Visitor Counts and Password Change
fields.</p>
<p>The Total Visitors count indicates how many times the Home Page has been viewed.
If the same computer has gone to the page 5 times, it will count as 5 visits. The
Unique Visitors only counts the same computer 1 time (based on IP address).  These 
counts can be reset with the &lt;Reset&nbsp;Counters&gt; button.</p>
<p>The Administration Password can be changed by typing in the current password, then
entering the new password twice (to guard against typos.) Finally, click &ltChange&nbsp;Password&gt;.</p>

<h3>Website Pages</h3>
<p>The links Home, About Us, Therapists, Services, and Patients represent the Website Pages.
Clicking on those links will pull up a duplicate of the Website with one important change.
Every piece of information that can be changed has been converted to a link.  To edit that
content, just click on it.  A form will appear allowing changes to be made. The following changes
are possible:</p>
<ul class="ul_main">
	<li>Text can be edited.</li>
	<li>Images can be replaced by uploading new photos.</li>
	<li>Bios of the employees can be changed, added, or deleted.</li>
	<li>A YouTube-based video can be entered on the About Us page.</li>
</ul>
<p>Directions are provided on each of the editing pages.</p>


<h3>Website</h3>
<p>Once changes have been made, clicking the Website link will open up the
Home Page of the site in a new tab/window so that the changes can be viewed.</p>

<h3>Logout</h3>
<p>When done working with the Administration pages, you should always logout before
leaving, for security reasons.  The system will keep you logged in until the
browser closes or the Logout link is clicked.</p>